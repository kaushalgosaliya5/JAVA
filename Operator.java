class Operator {

   public static void main(String args[]) {

     byte a=13,b=21;

     System.out.println(a & b);
     System.out.println(a | b);
     System.out.println(a ^ b);
     System.out.println(~a);
     System.out.println(~b);
     System.out.println(a >> 1);
     System.out.println(a >>> 1);
     System.out.println(a << 1);
  } 
 }